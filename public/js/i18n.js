/* ── Helpers ── */
function gcT(key) {
  var lang = window.GC_LANG || 'es';
  var dict = window.GC_TRANSLATIONS[lang] || window.GC_TRANSLATIONS['es'];
  return dict[key] !== undefined ? dict[key] : (window.GC_TRANSLATIONS['es'][key] || key);
}

